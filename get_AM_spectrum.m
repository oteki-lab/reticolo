    if length(wavelength) == 1 && AM(i,1) <= wavelength && wavelength <= AM(i+1,1)
        AM_intensity = AM(i,2) ;
        return ;
    end
end
P_AM = trapz(AM(imin:imax,1), AM(imin:imax,2)) ;


% in Reticolo. the electric field are normalized such as
% |E_reticolo|^2 = esp0 * c |E|^2.
% since the Air mass (AM) is in unit of W/m^2/um. and that for the impigning 
% plance wave the power is given by P = 0.5 * esp0 * c |E|^2 * Period^2.
% Therefore. the intensity of the incident electric field modulus is
% |E_inc_reticolo|^2 = 0.5 * AM ;

AM_intensity = interp1(AM(:,1), AM(:,2), wavelength, method) ;

% we perform a normalisation so that the AM power stay constant after
% interpolation:

AM_intensity = AM_intensity  * P_AM / trapz(wavelength, AM_intensity) ;

